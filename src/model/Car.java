package model;
 public class Car {
     private String model;
     private String name;
     private double engine;
     private String city;
     private boolean inARmenia;

     public String getModel() {
         return model;
     }

     public void setModel(String model) {
         this.model = model;
     }

     public String getName() {
         return name;
     }

     public void setName(String name) {
         this.name = name;
     }

     public double getEngine() {
         return engine;
     }

     public void setEngine(double engine) {
         this.engine = engine;
     }

     public String getCity() {
         return city;
     }

     public void setCity(String city) {
         this.city = city;
     }

     public boolean isInARmenia() {
         return inARmenia;
     }

     public void setInARmenia(boolean inARmenia) {
         this.inARmenia = inARmenia;
     }
 }

